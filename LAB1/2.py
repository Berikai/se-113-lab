# Define two variables number1 and number2 and prompt the user to enter their values.
# Then, define three variables sum, diff, and prod, and assign to them the summation,
# difference and multiplication results of number1 and number2, respectively. Finally, display
# on the screen, the values of number1, number2, sum, diff, and prod.

number1 = int(input("Enter the first value: "))
number2 = int(input("Enter the second value: "))

sum = number1 + number2
diff = number1 - number2
prod = number1 * number2

print("Number 1:", number1)
print("Number 2:", number2)
print("Sum:", sum)
print("Diff:", diff)
print("Prod:", prod)