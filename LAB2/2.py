# Define three variables number1, number2, and number3 and ask the user to enter three
# integer values for these variables. Then, calculate and print their average.

number1 = int(input('Enter 1st integer: '))
number2 = int(input('Enter 2nd integer: '))
number3 = int(input('Enter 3rd integer: '))

average = (number1 + number2 + number3) / 3

print(f'Average: {average}')